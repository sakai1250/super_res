import 'dart:io';
import 'dart:typed_data';
import 'package:flutter/material.dart';
import 'package:super_res/data/memo.dart';
import 'super_resolution_screen.dart';
import '../data/memo_dao.dart';

class PhotoDetailScreen extends StatefulWidget {
  final String imagePath;
  final Memo memo;

  PhotoDetailScreen({required this.imagePath, required this.memo});

  @override
  _PhotoDetailScreenState createState() => _PhotoDetailScreenState();
}

class _PhotoDetailScreenState extends State<PhotoDetailScreen> {
  final MemoDao _memoDao = MemoDao();
  late TextEditingController _textController;
  late File _displayedImage;

  @override
  void initState() {
    super.initState();
    final initialText = widget.memo.textContent?.isNotEmpty == true
        ? widget.memo.textContent!
        : "Êú™ÂÖ•Âäõ";
    _textController = TextEditingController(text: initialText);
    _displayedImage = File(widget.imagePath);
  }

  @override
  void dispose() {
    _textController.dispose();
    super.dispose();
  }

  void _updateImage(Uint8List upscaledImage) {
    setState(() {
      final tempDir = Directory.systemTemp;
      final updatedImage = File('${tempDir.path}/upscaled_image.png');
      updatedImage.writeAsBytesSync(upscaledImage);
      _displayedImage = updatedImage;
    });
  }

  Future<void> _saveMemo() async {
    final updatedText =
        _textController.text == "Êú™ÂÖ•Âäõ" ? null : _textController.text;

    final updatedMemo = Memo(
      id: widget.memo.id,
      title: widget.memo.title,
      imagePath: widget.memo.imagePath,
      createdAt: widget.memo.createdAt,
      folderId: widget.memo.folderId,
      textContent: updatedText,
    );

    await _memoDao.updateMemo(updatedMemo);

    showDialog(
      context: context,
      builder: (ctx) => AlertDialog(
        title: Text('‰øùÂ≠òÂÆå‰∫Ü'),
        content: Text('„É°„É¢„Çí‰øùÂ≠ò„Åó„Åæ„Åó„Åü'),
        actions: [
          TextButton(
            onPressed: () => Navigator.of(ctx).pop(),
            child: Text('OK'),
          ),
        ],
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('ÂÜôÁúüË©≥Á¥∞'),
        backgroundColor: Colors.white,
        foregroundColor: Colors.black,
        elevation: 1,
        actions: [
          IconButton(
            icon: Icon(Icons.save),
            onPressed: _saveMemo,
          ),
        ],
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(16),
        child: Column(
          children: [
            // „Ç∫„Éº„É†ÂèØËÉΩ„Å™ÁîªÂÉè
            // üëá„Åì„ÅÆÈÉ®ÂàÜ„ÇíÁΩÆ„ÅçÊèõ„Åà„Çã
            SizedBox(
              width: double.infinity,
              child: InteractiveViewer(
                panEnabled: true,
                boundaryMargin: const EdgeInsets.all(100),
                minScale: 1.0,
                maxScale: 5.0,
                child: Image.file(
                  _displayedImage,
                  width: MediaQuery.of(context).size.width,
                  fit: BoxFit.contain,
                  frameBuilder: (context, child, frame, wasSync) {
                    if (wasSync) return child;
                    return AnimatedOpacity(
                      opacity: frame == null ? 0 : 1,
                      duration: const Duration(milliseconds: 300),
                      curve: Curves.easeInOut,
                      child: child,
                    );
                  },
                ),
              ),
            ),
            const SizedBox(height: 24),
            // „É°„É¢ÂÖ•ÂäõÊ¨ÑÔºà‰∏ã„Å´ÈÖçÁΩÆÔºâ
            TextField(
              controller: _textController,
              maxLines: null,
              decoration: InputDecoration(
                labelText: '„É°„É¢„ÇíÁ∑®ÈõÜ',
                filled: true,
                fillColor: Colors.grey[100],
                border: OutlineInputBorder(
                  borderRadius: BorderRadius.circular(12),
                  borderSide: BorderSide.none,
                ),
                contentPadding: EdgeInsets.all(16),
              ),
            ),

            const SizedBox(height: 24),

            // Ë∂ÖËß£ÂÉè„Éú„Çø„É≥
            ElevatedButton(
              onPressed: () {
                Navigator.push(
                  context,
                  MaterialPageRoute(
                    builder: (_) => SuperResolutionScreen(
                      originalImage: _displayedImage,
                      onSave: _updateImage,
                    ),
                  ),
                );
              },
              style: ElevatedButton.styleFrom(
                backgroundColor: Theme.of(context).primaryColor,
                foregroundColor: Colors.white,
                elevation: 3,
                shape: RoundedRectangleBorder(
                  borderRadius: BorderRadius.circular(12),
                ),
                padding: EdgeInsets.symmetric(horizontal: 32, vertical: 16),
              ),
              child: Text(
                'Ë∂ÖËß£ÂÉè',
                style: TextStyle(fontWeight: FontWeight.bold, fontSize: 16),
              ),
            ),
          ],
        ),
      ),
    );
  }
}
